#include <iostream>
using namespace std;

int main() {
   int a = 21;
   int b = 10;
   int c ;
   c = a + b;
   cout << "addition is :" << c << endl ;

   c = a - b;
   cout << "sub is  :" << c << endl ;

   c = a * b;
   cout << "multiplication is :" << c << endl ;

   c = a / b;
   cout << "div is :" << c << endl ;

   c = a % b;
   cout << "mod is  :" << c << endl ;
   return 0;
}
